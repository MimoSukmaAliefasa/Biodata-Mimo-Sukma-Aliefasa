#include <iostream>
#include <stdio.h>
#include <stdlib.h>


int main()
{
    int i=0;

    //deskripsi
    //looping menggunakan "do while"
    do
    {
        printf("%d SELAMAT PAGI SAHABAT \n",i+1);
        i++;
    }
    while(i<30);
    return 0;
}
//kamus
main()
{
    int i=0;

    //deskripsi
    //looping menggunakan "for"
    for(i=0;i<30;i++)
    {
        printf("%d SELAMAT PAGI SAHABAT\n",i+1);
    }
    return 0;
}
